package neueda.tinyurl.controller;

public class Response {
	
		private String url;
		private String ipAddr;
		
		public String getUrl() {
			return url;
		}

		public void setResponseString(String url) {
			this.url = url;
		}

		public String getIpAddr() {
			return ipAddr;
		}

		public void setIpAddr(String ipAddr) {
			this.ipAddr = ipAddr;
		}

	
}
